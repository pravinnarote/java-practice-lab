package main;

public class Operater {
    public static void main(String[] args) {
            int  a = 200;
            int b = 500;

            int result = b + a;
            System.out.println(result);

            byte c = (byte) result;
            System.out.println(c);

            float f = result;
            System.out.println(f);
    }
}